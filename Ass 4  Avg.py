# This is a way of finding the average of five numbers
# These are the numbers
# Now we must tell python the numbers to use

my_numbers= [5, 15, 45, 200, 90]

#And then, just find the average
# To do it, just divide the total of the numbers
# By
list_avg = sum(my_numbers) / len(my_numbers)

print('The average of the numbers in the list is: ', list_avg)

